let myFuture = prompt("Type any random number between 1 and 5");

let myFuture2 = parseInt(myFuture);

if (myFuture2 === 1) {
  console.log("You will be a Doctor");
  
} else if (myFuture2 === 2) {
  console.log("You will be a Millionaire");

} else if (myFuture2 === 3) {
  console.log("You will be a Teacher");

} else if (myFuture2 === 4) {
  console.log("You will be a Billionaire");

} else if (myFuture2 === 5) {
  console.log("You will be a Loser");
  
} else {
  console.log("Please enter a number between 1 and 5");
}
